
module.exports = class Calculator{

    soma(x, y){
        const soma = x + y
        return soma;
    }

    subtracao(x, y){
        const sub = x - y
        return sub;
    }

    divisao(x, y) {
        const div = x / y
        return div;
    
    }

    multiplicacao(x, y){
        const multi = x * y
        return multi;
    }
    
}